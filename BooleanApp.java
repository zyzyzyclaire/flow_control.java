package study_java_flow_control;

public class BooleanApp {
	public static void main(String[] args) {
		
		System.out.println(true);
		System.out.println(false);
		
		String foo = "Hello, World";
		
		System.out.println(foo.contains("World"));
		System.out.println(foo.contains("World!!"));
		
	}
}
